/**
 *  ラベルの検索処理
 *
 *	指定されたラベルを検索し位置を返す。
 */
uint32_t GetLabelPointer(const char* label_find) {
	// (4-5.)ラベルを検査する。
	for (uint32_t i = 0; g_script.label_list[i].name != NULL; i++) {
		const char* label = g_script.label_list[i].name;
		if (strcmp(label_find, label) == 0) {
			// 検索するラベルが見つかったので位置を返す。
			return g_script.label_list[i].pointer;
		}
	}
	_ASSERTE(!"ラベルがありません");
	return 0;
}
