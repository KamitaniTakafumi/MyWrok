/**
 *  メッセージエリアの設定
 */
void ScriptSetMessageArea(uint32_t cx, uint32_t cy) {

	uint32_t left = cx - MSG_AREA_WIDTH / 2;
	uint32_t right = left + MSG_AREA_WIDTH;

	uint32_t head = cy - MSG_WINDOW_HEIGHT / 2;
	for (uint32_t i = 0; i < MSG_LINE_MAX; i++) {
		uint32_t top = head + MSG_GRID_HEIGHT * i;
		uint32_t bottom = top + MSG_AREA_HEIGHT;
		SetRect(&g_script.message[i].area, left, top, right, bottom);
	}
}
