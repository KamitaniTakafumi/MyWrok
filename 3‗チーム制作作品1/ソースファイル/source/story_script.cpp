/**
 *  時間待ち(フェードイン・アウト演出あり)
 */
void ScriptTimeWaitFadeInOut() {
	// メッセージ出力中
	if (g_script.message_is_output) {
		return;
	}
	// (3.-4.)時間待ち
	if (g_script.waiting > 0) {
		if (temp_flag == 0) {
			waitingtime_temp = g_script.waiting;	// ここで待ちフレームを記憶する
			temp_flag = 1;	// 何度も繰り返さないようにする
		}
		g_script.waiting--;

		/* フェードインさせる */
		if (FadeIn_Alpha < 255 && FadeOut_Alpha == 0) {
			FadeIn_Alpha += (255 / waitingtime_temp) * 3;
		}
		if (FadeIn_Alpha > 254 && FadeOut_Alpha == 0) {
			WaitTime++;		// ブランクを作る

			/* 一定時間たった後フェードアウトさせるボックスのα値を最大にする */
			if (WaitTime >= (waitingtime_temp / 3)) {
				FadeIn_Alpha = 0;
				FadeOut_Alpha = 255;
			}
		}

		/* フェードアウトさせる */
		if (FadeOut_Alpha > 1) {
			FadeOut_Alpha -= (255 / waitingtime_temp) * 3;
		}

	}
	else {
		FadeIn_Alpha = 0;
		FadeOut_Alpha = 0;
		WaitTime = 0;
		temp_flag = 0;
		waitingtime_temp = 0;
		ScriptSetState(SCRIPT_PARSING);
	}
}
