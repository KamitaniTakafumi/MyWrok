/**
 *  スクリプト制御状態
 */
typedef enum {
	SCRIPT_PARSING,		  ///< パーシング(解析)中
	SCRIPT_TIME_WAIT,		///< 時間待ち
	SCRIPT_TIME_WAIT_FADEINOUT, ///< 時間待ち（特殊演出あり)
	SCRIPT_TIME_WAIT_FADEIN, ///< 時間待ち（特殊演出あり)
	SCRIPT_TIME_WAIT_FADEOUT, ///< 時間待ち（特殊演出あり)
	SCRIPT_TIME_WAIT_SLIDEINOUT,
	SCRIPT_WINDOW_SHAKE,
	SCRIPT_CLICK_WAIT,	///< クリック待ち
	SCRIPT_CHOICE_WAIT, ///< 選択クリック待ち
	SCRIPT_END,			    ///< 終了
} ScriptState;
/**
 *  メッセージ用構造体
 */
typedef struct {
	bool is_cursor_over;  ///< カーソルとareaの重なり
	RECT area;            ///< 表示エリア矩形
	char str[MSG_STRING_MAX + 1];///< 表示文字列
	uint32_t color;       ///< 文字の色
	int32_t right_goal;   ///< 表示エリア右の座標
} Message;
/**
 *  描画用の構造体
 */
typedef struct {
	bool is_visible;///< カーソル表示
	int32_t	x, y;   ///< 描画座標
	uint32_t hundle;///< 画像のハンドル
} Draw;
/**
 *  再生用の構造体
 */
typedef struct
{
	bool is_visible;///< カーソル表示
	uint32_t hundle;///< 音源のハンドル
} Play;
/**
 *  ラベル用の構造体
 */
typedef struct {
	char name[LABEL_STRING_MAX + 1];  ///< ラベル文字列
	uint32_t pointer;                 ///< ラベルの位置
} Label;
/**
 *  画像用の構造体
 */
typedef struct {
	char tag_name[IMAGE_NAME_MAX + 1];  ///< 画像のタグ名
	char file_name[IMAGE_NAME_MAX + 1]; ///< 画像のファイル名
	uint32_t hundle;                    ///< 画像のハンドル
} Image;
/**
 *  画像用の構造体
 */
typedef struct
{
	char sound_name[SOUND_NAME_MAX + 1];  ///< 音源のタグ名
	char soundfile_name[SOUND_NAME_MAX + 1]; ///< 音源のファイル名
	uint32_t hundle;                    ///< 音源のハンドル
} Sound;
/**
 *  カーソル用構造体
 */
typedef struct {
	bool is_visible;  ///< カーソル表示
	int32_t	x, y;     ///< 座標
	uint32_t hundle;  ///< 画像のハンドル
} CURSOR;
/**
 *  スクリプト制御用構造体
 */
typedef struct {
	ScriptState state;    ///< スクリプト制御状態
	uint32_t pointer;     ///< スクリプト制御位置
	uint32_t waiting;     ///< 時間待ち
	char** lines;         ///< スクリプト格納領域各行へのポインタ
	char line[SCRIPT_LINE_MAX + 1]; ///< 作業用
	uint32_t line_length; ///< 割り当てた作業領域の長さ
	uint32_t lines_max;   ///< スクリプトの行数

	uint32_t choice_label[CHOICE_LINE_MAX];///< 選択肢の飛び先ラベル
	uint32_t choice_count;                 ///< 選択肢の数
	Message choice[CHOICE_LINE_MAX];       ///< 選択肢の文字列

	uint32_t name_count;                 ///< 名前の数
	Message name[NAME_LINE_MAX];       ///< 名前の文字列

	uint32_t message_count;       ///< メッセージの数
	Message message[MSG_LINE_MAX];///< メッセージの文字列
	bool message_is_output;       ///< メッセージ出力中

	bool is_click_wait_visible; ///< クリック待ちの表示
	uint32_t click_wait_hundle;    ///< クリック待ちの画像ハンドル

	CURSOR cursor;  ///< カーソル

	Draw draw_list[DRAW_LIST_MAX];    ///< 描画リスト
	Label label_list[LABEL_LIST_MAX]; ///< ラベルリスト

	uint32_t image_count;             ///< 画像の数
	Image image_list[IMAGE_LIST_MAX]; ///< 画像のリスト

	uint32_t sound_count;             ///< 音源の数
	Sound sound_list[SOUND_LIST_MAX]; ///< 音源のリスト

	Play play_list[PLAY_LIST_MAX];    ///< 再生リスト

	char* buffer;   ///< スクリプト格納領域へのポインタ
} ScriptParser;

