/**
 *  選択待ち
 */
void ScriptChoiceWait() {
	// (5-9)マウスの座標を取得する。
	POINT mouse = { g_script.cursor.x, g_script.cursor.y };

	// (5-10)選択肢とマウスの当たり判定をとる。
	for (uint32_t i = 0; i < CHOICE_LINE_MAX; i++) {
		g_script.choice[i].is_cursor_over = false;
		// 通常時の選択肢の色を設定
		g_script.choice[i].color = GetColor(64, 64, 255);
		// 矩形と点の当たり判定  
		if (!PtInRect(&g_script.choice[i].area, mouse)) {
			continue;
		}
		g_script.choice[i].is_cursor_over = true;
		// 選択時の選択肢の色を設定
		g_script.choice[i].color = GetColor(128, 128, 255);
	}
	// (5-11)クリック確認
	if (!InputMgrIsClick()) {
		return;
	}
	// (5.-12.)どの選択肢がクリックされたか調べる。
	for (uint32_t i = 0; i < g_script.choice_count; i++) {
		if (g_script.choice[i].is_cursor_over) {
			ScriptSetState(SCRIPT_PARSING);
			g_script.choice_count = 0;
			g_script.message_count = 0;
			// 選択したラベルの位置を設定する。
			g_script.pointer = g_script.choice_label[i];
		}
	}
}

