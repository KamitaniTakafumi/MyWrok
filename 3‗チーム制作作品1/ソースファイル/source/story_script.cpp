/**
 *  mコマンド：文字列出力設定処理
 */
void MessageSeting() {
	// (2-1)メッセージの取得
	const char* mess = strtok(NULL, ",");

	// (2-2)メッセージを設定
	g_script.message_count %= MSG_LINE_MAX;
	uint32_t i = g_script.message_count;
	strncpy(g_script.message[i].str, mess, MSG_STRING_MAX);

	// メッセージ表示エリアの幅を無くす。
	g_script.message[i].area.right = g_script.message[i].area.left;

	// 文字列の長さから実際の幅を計算する。
	uint32_t width = strlen(g_script.message[i].str);
	width *= (MSG_FONT_SIZE / 2);
	g_script.message[i].right_goal = g_script.message[i].area.left + width;

	g_script.message_is_output = true;
	g_script.message_count++;

	// 次のコマンドへ
	g_script.pointer++;
}
